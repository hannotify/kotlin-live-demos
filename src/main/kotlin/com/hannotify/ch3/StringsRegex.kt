fun parsePath(path: String) {
    val regex = "(.+)/(.+)\\.(.+)".toRegex()
    val anotherRegex = """(.+)/(.+)\.(.+)""".toRegex()

    val matchResult = regex.matchEntire(path)
    if (matchResult != null) {
        val (directory, filename, extension) = matchResult.destructured
        println("Dir: $directory, name: $filename, ext: $extension")
    }
}

